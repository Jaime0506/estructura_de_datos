public class GrafoBogota {
    public static void main(String[] args) {
        // Crear el grafo
        Graph<String, DefaultWeightedEdge> grafo = new DefaultUndirectedWeightedGraph<>(DefaultWeightedEdge.class);

        // Agregar los parques como vértices
        grafo.addVertex("Parque Metropolitano Simon Bolivar");
        grafo.addVertex("Parque Nacional Enrique Olaya");
        grafo.addVertex("Parque de la 93");
        grafo.addVertex("Parque de Usaquen");
        grafo.addVertex("Parque El Tunal");
        grafo.addVertex("Parque de la Independencia");
        grafo.addVertex("Parque Regional La Florida");
        grafo.addVertex("Parque San Jose de Bavaria");
        grafo.addVertex("Universidad Manuela Beltran");

        // Agregar aristas con pesos (distancias)
        // Aquí deberías establecer las distancias reales entre los parques y la Universidad Manuela Beltran
        // Usaré distancias ficticias como ejemplo
        grafo.setEdgeWeight(grafo.addEdge("Parque Metropolitano Simon Bolivar", "Parque Nacional Enrique Olaya"), 5);
        grafo.setEdgeWeight(grafo.addEdge("Parque Metropolitano Simon Bolivar", "Parque de la 93"), 7);
        grafo.setEdgeWeight(grafo.addEdge("Parque Metropolitano Simon Bolivar", "Parque de Usaquen"), 10);
        // ... agregar el resto de las conexiones con sus respectivos pesos

        // Configurar la visualización del grafo
        JGraphXAdapter<String, DefaultWeightedEdge> graphAdapter = new JGraphXAdapter<>(grafo);
        mxIGraphLayout layout = new mxCircleLayout(graphAdapter);
        layout.execute(graphAdapter.getDefaultParent());

        // Mostrar el grafo en un frame
        JFrame frame = new JFrame();
        frame.getContentPane().add(new mxGraphComponent(graphAdapter));
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        frame.setPreferredSize(new Dimension(800, 600));
        frame.pack();
        frame.setVisible(true);
    }
}